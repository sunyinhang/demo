package com.haiercash.payplatform.common.data;

public class PayApplyLog {
	private String id;
	private String channelno;
	private String applseq;
	private String flag;
	private String tradetime;
	private String tradecode;
	private String pushnum;
	private String applcde;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getChannelno() {
		return channelno;
	}
	public void setChannelno(String channelno) {
		this.channelno = channelno;
	}
	public String getApplseq() {
		return applseq;
	}
	public void setApplseq(String applseq) {
		this.applseq = applseq;
	}
	public String getFlag() {
		return flag;
	}
	public void setFlag(String flag) {
		this.flag = flag;
	}
	public String getTradetime() {
		return tradetime;
	}
	public void setTradetime(String tradetime) {
		this.tradetime = tradetime;
	}
	public String getTradecode() {
		return tradecode;
	}
	public void setTradecode(String tradecode) {
		this.tradecode = tradecode;
	}
	public String getPushnum() {
		return pushnum;
	}
	public void setPushnum(String pushnum) {
		this.pushnum = pushnum;
	}
	public String getApplcde() {
		return applcde;
	}
	public void setApplcde(String applcde) {
		this.applcde = applcde;
	}
	
}
